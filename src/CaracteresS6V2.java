
public class CaracteresS6V2 {
    public void PoliCodeCraftersS6V2(int nroTerminos) {

        System.out.println("Serie de caracteres 6.2: ");

        char letra = 'a';

        for (int i = 0; i < nroTerminos; i++) {
            System.out.print(letra + " ");
            letra++;
        }
    }
}
